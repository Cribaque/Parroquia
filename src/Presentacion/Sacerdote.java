    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (jTextMonto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor llene el campo");
        } else {
            JTable tabla;
            DefaultTableModel dfm = new DefaultTableModel();
            tabla = this.jTable1;
            tabla.setModel(dfm);

            dfm.setColumnIdentifiers(new Object[]{"Id", "Nombre", "Cantidad", "Ubicacion", "Estado", "Marca", "Precio"});
            ResultSet rs = Negocios.Inventario.inventario_sobre_x(Integer.parseInt(jTextMonto.getText()));
            try {
                while (rs.next()) {
                    dfm.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getInt(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getInt(8)});
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error");
            }
            conectarMysql.desconectar();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

