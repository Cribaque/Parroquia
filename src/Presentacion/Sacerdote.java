        tabla.setModel(dfm);

        dfm.setColumnIdentifiers(new Object[]{"Id Parrroquia", "Nombre", "Id Diocesis", "Diocesis", "Ciudad", "Pais"});
        ResultSet rs = Negocios.Parroquia.consultarDatos();
